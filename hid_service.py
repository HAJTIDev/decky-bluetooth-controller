class BluetoothHIDService(dbus.service.Object):
    PATH = '/org/bluez/hid'
    
    def __init__(self):
        bus_name = dbus.service.BusName('org.bluez', bus=dbus.SystemBus())
        dbus.service.Object.__init__(self, bus_name, self.PATH)
        
        # Register as HID device
        self.register_hid_device()
    
    def register_hid_device(self):
        """Register as Bluetooth HID device"""
        try:
            # Create HID descriptor file
            hid_desc = self.create_hid_descriptor()
            desc_path = "/tmp/steamdeck_hid_desc"
            
            with open(desc_path, 'wb') as f:
                f.write(hid_desc)
            
            # Use sdptool to add HID service record
            subprocess.run([
                "sdptool", "add", "--channel=1", "HID"
            ], capture_output=True)
            
            print("âœ… HID service registered on channel 1")
            return True
            
        except Exception as e:
            print(f"Error registering HID: {e}")
            return False
    
